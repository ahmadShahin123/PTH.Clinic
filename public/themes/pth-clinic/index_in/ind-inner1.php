				<p dir="rtl">
	من الأخطاء الشائعة أن لون الدم في الأوردة لونه أزرق بسبب أنه يحمل الهيموجلوبين الغير مأكسد، وهذا غير صحيح، لأن الوريد لا يحمل هيموجلوبين غير مأكسد، بل يحمل هيموجلوبين يحتوي على الأكسجين ولكن بنسبة أقل من الشرايين. (الوريد هو الوعاء الدموي الذي يحمل الدم المحمل بالهيموجلوبين المأكسد من القلب إلى الجسم، والوريد هو الوعاء الدموي الذي يحمل الدم الغير مؤكسد من مختلف أعضاء الجسم إلى القلب).
</p>
<p dir="rtl">
	<span style="color:#3366cc;"><strong>إذا لماذا الأوردة تظهر باللون الأزرق؟</strong></span>
</p>
<p dir="rtl">
	بعد البحث عن إجابة مناسبة، اختلفت العلماء في تحديد السبب ولكن هنالك عدة عوامل تفسر هذه الظاهرة:
</p>
<ol dir="rtl">
<li>
		&nbsp;أحدها أن السبب بظهور الدماء باللون الأزرق هو وجود طبقة الجلد العازلة للضوء والتي تمنع امتصاص الضوء حسب الطول الموجي للألوان، وبعد وصول الضوء الأزرق يمتص الجلد هذا الضوء أكثر من الضوء الأحمر، والأوردة (أو الأوعية الدموية بشكل عام) تمتص اللون الأزرق بشكل أكبر من الأحمر،. فيكون انعكاس هذا الضوء هو الظاهر ولذلك تظهر الأوردة باللون الأزرق.
	</li>
<li>
		الدم الغير مؤكسد يكون بالعادة غامق اللون، ما يجعله أكثر ظهورًا تحت الجلد، ولو كانت الشرايين قريبة من سطح الجلد أو كانت أقل سماكة (الوعاء الشرياني يكون أسمك من الوعاء الوريدي لأنه يحمل دماءً بضغط عالي من القلب)، ولو استطعنا رؤية الشريان من تحت الجلد لكان لونه أزرقًا كذلك. ولكن اللون الحقيقي للدم في الأوردة والشرايين هو اللون الأحمر القاتم أو الداكن.
	</li>
<li>
		تختلف الألوان (أزرق أو أخضر) من شخص لآخر أو من مكان بالجسم لآخر لأن الأوردة لها سُمك مختلف وأحجام مختلفة، فيكون امتصاص الطول الموجي للون مختلفًا من مكان لآخر.
	</li>
<li>
		السبب الأخير هو تفسير اللون حسب إدراك الدماغ، وذلك بسبب أن الدماغ قد يرى الأوردة بشكل أغمق (أزرق) من العادي لتمييزه من اللون الفاتح (لون الجلد) مع اختلاف الإضاءة،&nbsp;وذلك أشبه بتفسير بعض الخدع البصرية، فيكون اللون أزرقٍا من العادة.
	</li>
</ol>
