	السلام عليكم انا شاب عمري ٢٠ سنة على وشك عملية تجميلية للأنف لأنني تعرضت لضربة في مشاجرة قبل ٤ سنوات وأعاني من انتفاخ وتغير في شكله الخارجي. لكن الشيء المزعج انني <a href="tag/-d9-85-d8-af-d8-ae-d9-86/-d9-85-d8-af-d8-ae-d9-86.html" class="st_tag internal_tag" rel="tag" title="Posts tagged with مدخن">مدخن</a> وقال لي الدكتور بايقاف التدخين قبل شهر من العملية وانا الان ما زلت ادخن وعمليتي في ٥ ايام. ماذا سوف يحدث؟ وهل من المممكن فشل العملية</p>
