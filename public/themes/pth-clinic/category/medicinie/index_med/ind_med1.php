				<p style="direction: rtl; ">
	عند فحص التهاب الكبد الوبائي B. يتم عمل ثلاث فحوصات وهي تشمل:
</p>
<ol dir="rtl">
<li>
		HBsAg وهو فحص لسطح&nbsp;مولد المضاد.
	</li>
<li>
		HBsAb وهو فحص لسطح المضاد.
	</li>
<li>
		HBcAb وهو فحص للمضاد نفسه.
	</li>
</ol>
<p dir="rtl">
	&nbsp;
</p>
<p dir="rtl">
	بالنسبة للفحص الأول، والذي ذكرته في السؤال، فإذا ظهرت النتيجة موجبة فهذا يعني أنك تعاني حاليا من التهاب الكبد (قد يكون هذا الالتهاب جديداً، أو قد يكون مزمناً).
</p>
<p dir="rtl">
	بالنسبة لأن تعود النتيجة إلى النتيجة الطبيعيّة، فهذا يعتمد. فإذا كان الالتهاب جديداً وحدث مؤخراً، فحينها قد يذهب لوحده بعد مرور بضعة أسابيع، وتتعافى منه تماماً، وليس هناك حاجة لتناول أي أدوية، فقط راحة وتناول السوائل.
</p>
<p dir="rtl">
	ولكن إذا كان الالتهاب مزمناً وحدث منذ فترة طويلة، فحينها يمكن اللجوء إلى بعض الأدوية للتقليل من المضاعفات التي قد تحدث (بالإضافة إلى أن هناك نسبة من الأشخاص يتعافون من التهاب الكبد الوبائي المزمن).
</p>
<p dir="rtl">
	أنصحك بالمتابعة مع طبيب باطنة، للتأكد من هذا الاتهاب.
</p>
<p dir="rtl">
	أتمنّى لك دوام الصحّة والعافية.
</p>
